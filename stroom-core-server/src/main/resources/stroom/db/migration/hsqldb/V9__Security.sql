-- User
create table USR (ID INT generated by default as identity (start with 1), VER TINYINT not null, CRT_DT TIMESTAMP not null, CRT_USER varchar(255), UPD_DT TIMESTAMP not null, UPD_USER varchar(255), FK_FOLDER_ID INT, UUID varchar(255) not null, NAME varchar(255) not null, CUR_LOGIN_FAIL SMALLINT not null, GRP boolean not null, LAST_LOGIN_MS BIGINT, LOGIN_EXPIRY boolean, LOGIN_VALID_MS BIGINT, PASS_EXPIRY_MS BIGINT, PASS_HASH varchar(255), STAT tinyint not null, TOTL_LOGIN_FAIL SMALLINT not null, primary key (ID));
alter table USR add constraint UK_USR_NAME_GRP unique (NAME, GRP);
alter table USR add constraint UK_USR_UUID unique (UUID);

-- User Group User
create table USR_GRP_USR (ID BIGINT generated by default as identity (start with 1), VER TINYINT not null, GRP_UUID varchar(255) not null, USR_UUID varchar(255) not null, primary key (ID));
alter table USR_GRP_USR add constraint UK_bro5c2icvkgs7g3x4xuddmlo0  unique (GRP_UUID, USR_UUID);

-- Permission
create table PERM (ID INT generated by default as identity (start with 1), VER TINYINT not null, NAME varchar(255) not null, primary key (ID));
alter table PERM add constraint UK_8omoyycj6qqvoywrvhq7v5y6k  unique (NAME);

-- Application Permission
create table APP_PERM (ID BIGINT generated by default as identity (start with 1), VER TINYINT not null, USR_UUID varchar(255) not null, FK_PERM_ID INT not null, primary key (ID));
alter table APP_PERM add constraint UK_r4fkwgng00rjbuqhlxr6okxbh  unique (USR_UUID, FK_PERM_ID);

-- Document Permission
create table DOC_PERM (ID BIGINT generated by default as identity (start with 1), VER TINYINT not null, USR_UUID varchar(255) not null, DOC_TP varchar(255) not null, DOC_UUID varchar(255) not null, PERM varchar(255) not null, primary key (ID));
alter table DOC_PERM add constraint UK_61wobb2b7lrephr2c7v08n6kw  unique (USR_UUID, DOC_TP, DOC_UUID, PERM);